            sb.AppendLine("                        }");
            sb.AppendLine("                    }");
            sb.AppendLine("                },");
            sb.AppendLine("                scales: {");
            sb.AppendLine("                    x: {");
            sb.AppendLine("                        grid: { color: '#1e293b' },");
            sb.AppendLine("                        ticks: { color: '#94a3b8' }");
            sb.AppendLine("                    },");
            sb.AppendLine("                    y: {");
            sb.AppendLine("                        grid: { display: false },");
            sb.AppendLine("                        ticks: { color: '#f8fafc', font: { weight: 'bold' } }");
            sb.AppendLine("                    }");
            sb.AppendLine("                }");
            sb.AppendLine("            }");
            sb.AppendLine("        });");
            sb.AppendLine("        ");
            sb.AppendLine("        // Dynamic Goal Calibration Engine");
            sb.AppendLine("        let currentMultiplier = 100;");
            sb.AppendLine("        const rdaSlider = document.getElementById('rdaScaleSlider');");
            sb.AppendLine("        const scaleValueText = document.getElementById('scaleValue');");
            sb.AppendLine("        const presetBtns = document.querySelectorAll('.preset-btn');");
            sb.AppendLine("        ");
            sb.AppendLine("        function updateRdaScale(multiplier) {");
            sb.AppendLine("            currentMultiplier = multiplier;");
            sb.AppendLine("            rdaSlider.value = multiplier;");
            sb.AppendLine("            ");
            sb.AppendLine("            let suffix = \" (Standard)\";");
            sb.AppendLine("            if (multiplier < 100) suffix = ` (${100 - multiplier}% Deficit Protocol)`;");
            sb.AppendLine("            if (multiplier > 100) suffix = ` (+${multiplier - 100}% Loading Protocol)`;");
            sb.AppendLine("            scaleValueText.textContent = `${multiplier}%${suffix}`;");
            sb.AppendLine("            ");
            sb.AppendLine("            presetBtns.forEach(btn => {");
            sb.AppendLine("                if (parseInt(btn.dataset.scale) === multiplier) {");
            sb.AppendLine("                    btn.classList.add('active');");
            sb.AppendLine("                } else {");
            sb.AppendLine("                    btn.classList.remove('active');");
            sb.AppendLine("                }");
            sb.AppendLine("            });");
            sb.AppendLine("            ");
            sb.AppendLine("            // Update HTML cells dynamically");
            sb.AppendLine("            nutrientStatuses.forEach(s => {");
            sb.AppendLine("                const row = document.querySelector(`tr[data-nutrient-key=\"${s.key}\"]`);");
            sb.AppendLine("                if (row) {");
            sb.AppendLine("                    const scaledRda = s.rda * (multiplier / 100);");
            sb.AppendLine("                    const rdaCell = row.querySelector('.nutrient-rda-val');");
            sb.AppendLine("                    if (rdaCell) {");
            sb.AppendLine("                        rdaCell.textContent = scaledRda.toFixed(1) + \" \" + s.unit;");
            sb.AppendLine("                    }");
            sb.AppendLine("                    ");
            sb.AppendLine("                    const pct = scaledRda > 0 ? (s.intake / scaledRda * 100) : 0;");
            sb.AppendLine("                    const pctTextCell = row.querySelector('.nutrient-pct-text');");
            sb.AppendLine("                    if (pctTextCell) {");
            sb.AppendLine("                        pctTextCell.textContent = pct.toFixed(0) + \"%\";");
            sb.AppendLine("                    }");
            sb.AppendLine("                    ");
            sb.AppendLine("                    const progressBar = row.querySelector('.progress-bar');");
            sb.AppendLine("                    if (progressBar) {");
            sb.AppendLine("                        progressBar.style.width = Math.min(pct, 100.0).toFixed(0) + \"%\";");
            sb.AppendLine("                        progressBar.className = \"progress-bar\";");
            sb.AppendLine("                        if (s.isMaxLimit) {");
            sb.AppendLine("                            if (s.intake > scaledRda && scaledRda > 0) progressBar.classList.add('progress-deficit');");
            sb.AppendLine("                            else progressBar.classList.add('progress-optimal');");
            sb.AppendLine("                        } else {");
            sb.AppendLine("                            if (pct < 50) progressBar.classList.add('progress-deficit');");
            sb.AppendLine("                            else if (pct < 100) progressBar.classList.add('progress-suboptimal');");
            sb.AppendLine("                            else progressBar.classList.add('progress-optimal');");
            sb.AppendLine("                        }");
            sb.AppendLine("                    }");
            sb.AppendLine("                    ");
            sb.AppendLine("                    const badge = row.querySelector('.badge');");
            sb.AppendLine("                    if (badge) {");
            sb.AppendLine("                        badge.className = \"badge\";");
            sb.AppendLine("                        let statusText = \"Optimal\";");
            sb.AppendLine("                        if (s.isMaxLimit) {");
            sb.AppendLine("                            if (s.intake > scaledRda && scaledRda > 0) {");
            sb.AppendLine("                                statusText = \"Exceeded\";");
            sb.AppendLine("                                badge.classList.add('badge-deficit');");
            sb.AppendLine("                            } else {");
            sb.AppendLine("                                badge.classList.add('badge-optimal');");
            sb.AppendLine("                            }");
            sb.AppendLine("                        } else {");
            sb.AppendLine("                            if (pct < 50) {");
            sb.AppendLine("                                statusText = \"Deficient\";");
            sb.AppendLine("                                badge.classList.add('badge-deficit');");
            sb.AppendLine("                            } else if (pct < 100) {");
            sb.AppendLine("                                statusText = \"Sub-Optimal\";");
            sb.AppendLine("                                badge.classList.add('badge-suboptimal');");
            sb.AppendLine("                            } else {");
            sb.AppendLine("                                badge.classList.add('badge-optimal');");
            sb.AppendLine("                            }");
            sb.AppendLine("                        }");
            sb.AppendLine("                        badge.textContent = statusText;");
            sb.AppendLine("                    }");
            sb.AppendLine("                }");
            sb.AppendLine("            });");
            sb.AppendLine("            ");
            sb.AppendLine("            // Update bar chart data limit bar dynamically");
            sb.AppendLine("            nutrientBarChart.data.datasets[1].data = barNutrients.map(s => s.rda * (multiplier / 100));");
            sb.AppendLine("            nutrientBarChart.update();");
            sb.AppendLine("        }");
            sb.AppendLine("        ");
            sb.AppendLine("        rdaSlider.addEventListener('input', (e) => {");
            sb.AppendLine("            updateRdaScale(parseInt(e.target.value));");
            sb.AppendLine("        });");
            sb.AppendLine("        ");
            sb.AppendLine("        presetBtns.forEach(btn => {");
            sb.AppendLine("            btn.addEventListener('click', () => {");
            sb.AppendLine("                updateRdaScale(parseInt(btn.dataset.scale));");
            sb.AppendLine("            });");
            sb.AppendLine("        });");
            sb.AppendLine("        ");
            sb.AppendLine("        // Food Logs Interactive Live Filtering");
            sb.AppendLine("        const foodSearchInput = document.getElementById('foodSearch');");
            sb.AppendLine("        const mealFilterSelect = document.getElementById('mealFilter');");
            sb.AppendLine("        const logRows = document.querySelectorAll('.food-log-row');");
            sb.AppendLine("        ");
            sb.AppendLine("        function filterFoodLogs() {");
            sb.AppendLine("            const query = foodSearchInput.value.toLowerCase().trim();");
            sb.AppendLine("            const mealType = mealFilterSelect.value;");
            sb.AppendLine("            let visibleCount = 0;");
            sb.AppendLine("            ");
            sb.AppendLine("            logRows.forEach(row => {");
            sb.AppendLine("                const name = row.dataset.foodName.toLowerCase();");
            sb.AppendLine("                const meal = row.dataset.mealType;");
            sb.AppendLine("                const matchesQuery = name.includes(query);");
            sb.AppendLine("                const matchesMeal = mealType === 'all' || meal === mealType;");
            sb.AppendLine("                ");
            sb.AppendLine("                if (matchesQuery && matchesMeal) {");
            sb.AppendLine("                    row.style.display = '';");
            sb.AppendLine("                    visibleCount++;");
            sb.AppendLine("                } else {");
            sb.AppendLine("                    row.style.display = 'none';");
            sb.AppendLine("                }");
            sb.AppendLine("            });");
            sb.AppendLine("            ");
            sb.AppendLine("            const emptyState = document.getElementById('logsEmptyState');");
            sb.AppendLine("            if (emptyState) {");
            sb.AppendLine("                if (visibleCount === 0) {");
            sb.AppendLine("                    emptyState.style.display = '';");
            sb.AppendLine("                } else {");
            sb.AppendLine("                    emptyState.style.display = 'none';");
            sb.AppendLine("                }");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
            sb.AppendLine("        ");
            sb.AppendLine("        if (foodSearchInput) foodSearchInput.addEventListener('input', filterFoodLogs);");
            sb.AppendLine("        if (mealFilterSelect) mealFilterSelect.addEventListener('change', filterFoodLogs);");
            sb.AppendLine("    </script>");
            sb.AppendLine("</body>");
            sb.AppendLine("</html>");

            return sb.ToString();
        }
