            sb.AppendLine("                                const currentRda = s.rda * (currentMultiplier / 100);");
            sb.AppendLine("                                const pct = currentRda > 0 ? (s.intake / currentRda * 100).toFixed(0) : 0;");
            sb.AppendLine("                                return `Progress: ${pct}% of Calibrated Target`;");
