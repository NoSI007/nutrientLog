    fun findMatch(input: String): LookupFood? {
        val clean = input.lowercase().trim()
        return ITEMS.find { 
            clean.contains(it.name.lowercase()) || 
            it.name.lowercase().contains(clean) || 
            (clean.contains("egg") && it.name == "Whole Eggs") ||
            (clean.contains("oat") && it.name == "Organic Oatmeal") ||
            (clean.contains("salmon") && it.name == "Wild Atlantic Salmon") ||
            (clean.contains("chicken") && it.name == "Grilled Chicken Breast") ||
            (clean.contains("avocado") && it.name == "Fresh Hass Avocado") ||
            (clean.contains("banana") && it.name == "Banana") ||
            (clean.contains("steak") && it.name == "Pan-Seared NY Ribeye Beef Steak") ||
            (clean.contains("beef") && it.name == "Pan-Seared NY Ribeye Beef Steak") ||
